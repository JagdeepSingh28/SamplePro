public class POJO
{
    public int integer;
    public String string;
    public Boolean bool;
    public Float f;

    public POJO(int integer, String string, Boolean bool, Float f)
    {
        this.integer = integer;
        this.string = string;
        this.bool = bool;
        this.f = f;
    }
}